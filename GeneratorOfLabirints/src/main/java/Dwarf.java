public class Dwarf {
    private int pointX;
    private int pointY;

    public Dwarf() {
        this.pointX = 1;
        this.pointY = 1;
    }

    public int getPointX() {
        return pointX;
    }

    public void setPointX(int pointX) {
        this.pointX = pointX;
    }

    public int getPointY() {
        return pointY;
    }

    public void setPointY(int pointY) {
        this.pointY = pointY;
    }
}
